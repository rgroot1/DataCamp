# Define report_status
def report_status(**kwargs):
    """Print out the status of a movie character."""

    print("\nBEGIN: REPORT\n")

    # Print a formatted status report
    for key, value in kwargs.items():
        print(key + ": " + value)

    print("\nEND REPORT")

